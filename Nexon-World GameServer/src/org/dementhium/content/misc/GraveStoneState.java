package org.dementhium.content.misc;

/**
 * Represents the possible grave stone states.
 *
 * @author Emperor
 */
public enum GraveStoneState {

    /**
     * If the gravestone has been created.
     */
    CREATED,

    /**
     * If the gravestone is broken.
     */
    BROKEN,

    /**
     * If the gravestone is collapsed.
     */
    COLLAPSED,

    /**
     * If the gravestone has been blessed.
     */
    BLESSED;

}