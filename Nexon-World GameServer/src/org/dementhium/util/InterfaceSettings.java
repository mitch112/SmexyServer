	public static void switchWindow(Player p, int mode) {
		if (mode >= 2) {
			p.getConnection().setDisplayMode(mode);
			// ActionSender.sendWindowsPane(p, 746, 0);
			// normalIFaces(p);
			sendInterfaces(p);
			sendFullScreenAMasks(p);
		} else {
			p.getConnection().setDisplayMode(mode);
			// ActionSender.sendWindowsPane(p, 548, 0);
			// fullIFaces(p);
			sendInterfaces(p);
			sendFixedAMasks(p);
		}
	}

	public static void disableTab(Player player, int tab) { // 128
		int pane = player.getConnection().getDisplayMode() >= 2 ? 746 : 548;
		int child = tab < 8 ? (pane == 746 ? 36 : 128)
				: (pane == 746 ? 44 : 97);
		if (tab < 8) {
			ActionSender.sendInterfaceConfig(player, pane, child + tab, false);
			ActionSender.sendInterfaceConfig(player, pane, child + 9 + tab,
					false);
		} else {
			ActionSender.sendInterfaceConfig(player, pane, child + tab - 7,
					false);
			ActionSender
					.sendInterfaceConfig(player, pane, 105 + tab - 7, false);
		}
	}

	public static void enableTab(Player player, int tab) { // 128
		int pane = player.getConnection().getDisplayMode() >= 2 ? 746 : 548;
		int child = tab < 8 ? (pane == 746 ? 36 : 128)
				: (pane == 746 ? 44 : 97);
		if (tab < 8) {
			ActionSender.sendInterfaceConfig(player, pane, child + tab, true);
			ActionSender.sendInterfaceConfig(player, pane, child + 9 + tab,
					true);
		} else {
			ActionSender.sendInterfaceConfig(player, pane, child + tab - 7,
					true);
			ActionSender.sendInterfaceConfig(player, pane, 105 + tab - 7, true);
		}
	}
