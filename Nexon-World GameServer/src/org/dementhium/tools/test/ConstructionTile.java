public class ConstructionTile {

	private int xMod;
	private int yMod;
	private String type;
	private int index;
	/**
	 * @return the xMod
	 */
	public int getxMod() {
		return xMod;
	}
	/**
	 * @param xMod the xMod to set
	 */
	public void setxMod(int xMod) {
		this.xMod = xMod;
	}
	/**
	 * @return the yMod
	 */
	public int getyMod() {
		return yMod;
	}
	/**
	 * @param yMod the yMod to set
	 */
	public void setyMod(int yMod) {
		this.yMod = yMod;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the index
	 */
	public int getIndex() {
		return index;
	}
	/**
	 * @param index the index to set
	 */
	public void setIndex(int index) {
		this.index = index;
	}
	
}
